package model;

public class Orden {

	private String orden, tiempo,num;
	

	public Orden(String orden, String tiempo) {
		super();
		this.orden = orden;
		this.tiempo = tiempo;
	}

	public Orden() {

	}

	public String getOrden() {
		return orden;
	}

	public void setOrden(String orden) {
		this.orden = orden;
	}

	public String getTiempo() {
		return tiempo;
	}

	public void setTiempo(String tiempo) {
		this.tiempo = tiempo;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	
	

	
	
	
	
	

	
}
